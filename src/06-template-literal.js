// template literals are written covered with backticks ``
// we can embed any kind of variable inside a template literal string
// variables should be inside ${variable_name}

var name = "adarsh";
var surname = "pandya";

// normal way
console.log(name + " " + surname);

// template literal
console.log(`${name} ${surname}`);
